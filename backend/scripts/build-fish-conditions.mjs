/**
 * build-fish-conditions.mjs — generates src/fishConditions.js: per-fish catch
 * conditions (weather sets, ET hour windows, bait) for every fish in our
 * catalog that has restrictions.
 *
 * Sources:
 *  - ff14-fish-tracker-app compiled data (icykoneko, the community-standard
 *    big-fish tracker): weatherSet/previousWeatherSet/startHour/endHour/bait
 *  - Teamcraft items.json: item id → English name
 *  - Garland skywatcher weatherIndex: weather id → English name
 *
 * Run after fishing-catalog changes:  node backend/scripts/build-fish-conditions.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { FISHING_SPOTS } from '../../src/fishingData.js'

const here = path.dirname(fileURLToPath(import.meta.url))

const [dataJs, items, core] = await Promise.all([
  fetch('https://raw.githubusercontent.com/icykoneko/ff14-fish-tracker-app/master/js/app/data.js').then((r) => r.text()),
  fetch('https://raw.githubusercontent.com/ffxiv-teamcraft/ffxiv-teamcraft/master/libs/data/src/lib/json/items.json').then((r) => r.json()),
  fetch('https://www.garlandtools.org/db/doc/core/en/3/data.json').then((r) => r.json()),
])

// data.js is `const DATA = {...};` — evaluate the object literal.
const objText = dataJs.slice(dataJs.indexOf('{'), dataJs.lastIndexOf('}') + 1)
const DATA = new Function(`return ${objText}`)()
const weatherName = (id) => core.skywatcher.weatherIndex[id] || `weather#${id}`
const itemName = (id) => items[id]?.en || null

// name (lowercase) → tracker fish entry
const byName = new Map()
for (const [id, fish] of Object.entries(DATA.FISH)) {
  const n = itemName(Number(id))
  if (n) byName.set(n.toLowerCase(), fish)
}

const ourFish = new Set()
for (const s of FISHING_SPOTS) for (const f of s.fish) ourFish.add(f.name)

const out = {}
let restricted = 0
for (const name of [...ourFish].sort()) {
  const fish = byName.get(name.toLowerCase())
  if (!fish) continue
  const hasWeather = fish.weatherSet?.length > 0
  const hasHours = !(fish.startHour === 0 && fish.endHour === 24)
  if (!hasWeather && !hasHours) continue
  const bait = (fish.bestCatchPath || []).map(itemName).filter(Boolean)
  out[name] = {
    weather: (fish.weatherSet || []).map(weatherName),
    prevWeather: (fish.previousWeatherSet || []).map(weatherName),
    start: fish.startHour ?? 0,
    end: fish.endHour ?? 24,
    ...(bait.length ? { bait } : {}),
  }
  restricted++
}

const js = `/* Auto-generated by backend/scripts/build-fish-conditions.mjs — DO NOT EDIT BY HAND.
   Catch conditions for restricted fish in our catalog: required weather
   (current / previous Eorzean weather period), ET hour window, bait chain.
   Consumed by src/eorzeaWeather.js nextFishWindow() for live countdowns.
   Source: ff14-fish-tracker-app + Teamcraft + Garland. */

export const FISH_CONDITIONS = ${JSON.stringify(out, null, 2)}
`
fs.writeFileSync(path.join(here, '..', '..', 'src', 'fishConditions.js'), js)
console.log(`wrote src/fishConditions.js: ${restricted} restricted fish (of ${ourFish.size} in catalog)`)
